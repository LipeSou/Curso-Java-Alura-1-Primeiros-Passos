
public class ExercicioLacos {
	public static void main (String[] args) {
		//Imprimir os numeros 1 a 10
		//laço while
		int contador = 1;
		
		while (contador <= 10 ) {		
			System.out.println(contador);
			contador++;
		}	
		
		//Um laco for que imprima de 1 a 10
		
		for(contador= 1 ; contador <= 10 ; contador++) {
			System.out.println(contador);
		}
	}
}
