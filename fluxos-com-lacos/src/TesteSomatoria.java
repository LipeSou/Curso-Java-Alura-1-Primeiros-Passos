
public class TesteSomatoria {
	public static void main (String[] args) {
		//Faremos uma somatoria com os numeros de 0 a 10
		
		int contador = 0;
		int total = 0;
		
		while(contador <= 10) {
			
			total = total + contador;
			System.out.println(total);
			contador++;
		}
		
		
		
	}
}
